# 🔢 Tipo int → números inteiros
# Inteiros são números sem parte decimal, positivos ou negativos
# Exemplos:
# print(42)     # ➕ int positivo
# print(-17)    # ➖ int negativo
# print(0)      # 🟰 zero também é int

# 🌊 Tipo float → números com ponto flutuante
# Representam valores com parte decimal (mesmo que seja .0)
# Exemplos:
# print(3.14, -2.71)  # 📐 float positivo e negativo
# print(0.0)          # 🧊 zero em forma de float

# 🔍 type() → mostra o tipo inferido de um valor
print(type("Python"))                 # 🧵 string
print(type(100))                      # 🔢 inteiro
print(type(3.5), type(-8.0), type(0.0))  # 🌊 flutuantes
