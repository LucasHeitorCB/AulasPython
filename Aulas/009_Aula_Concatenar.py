# 🔗 Concatenando strings com o operador +
nome_completo = 'Maria' + ' ' + 'Silva'
print(nome_completo)

# 🔁 Repetindo strings com o operador *
linha_asteriscos = '*' * 20      # 🌟 linha de asteriscos
eco_nome = 'Maria ' * 3          # 📣 nome repetido 3 vezes

print(linha_asteriscos)
print(eco_nome)
