# ➕ Adição
soma = 7 + 13
print('Adição:', soma)

# ➖ Subtração
diferenca = 20 - 8
print('Subtração:', diferenca)

# ✖️ Multiplicação
produto = 4 * 6
print('Multiplicação:', produto)

# ➗ Divisão real (com casas decimais)
divisao_real = 9 / 2
print('Divisão:', divisao_real)

# 🔢 Divisão inteira (sem casas decimais)
div_inteira = 9 // 2
print('Divisão inteira:', div_inteira)

# 🚀 Exponenciação
potencia = 3 ** 3
print('Exponenciação:', potencia)

# 🧮 Módulo (resto da divisão)
resto = 17 % 3
print('Módulo:', resto)

# 🔍 Testando múltiplos com módulo
print(12 % 4 == 0)   # ✅ múltiplo de 4?
print(13 % 4 == 0)   # ❌ múltiplo de 4?
print(15 % 3 == 0)   # ✅ múltiplo de 3?
print(14 % 2 == 0)   # ✅ número par?
print(13 % 2 == 0)   # ❌ número ímpar?
