"""
📘 DocString informativa:
Linguagem: Python
Tipagem: Dinâmica / Forte
Tipo: str (string) = texto
Texto = Qualquer coisa entre aspas simples ou duplas
"""

print(9876)  # 🖨️ Apenas um número sendo impresso

# 🧵 Usando aspas simples
print('Maria Clara')
print(10, 'Maria "Clara"')  # Aspas duplas dentro de aspas simples

# 🧵 Usando aspas duplas
print("João Pedro")
print(20, "João 'Pedro'")  # Aspas simples dentro de aspas duplas

# 🔐 Usando escape com barra invertida
print("Ele disse: \"Olá, mundo!\"")  # Escapando aspas duplas

# 🪪 Raw string (ignora escapes)
print(r"Caminho: C:\novo\teste")  # Mostra o texto exatamente como está
