# ✅ Tipo bool (booleano)
# Respostas lógicas: Verdadeiro ou Falso (True / False)
# Muito usado em decisões, comparações e condições

# 🧮 Comparações com ==
print(5 == 5)    # ✔️ Sim → True
print(3 == 7)    # ❌ Não → False

# 🔎 A função type mostra o tipo inferido
print(type(False))            # 🧠 tipo bool
print(type(True))             # 🧠 tipo bool
print(type(2 == 2))           # 🎯 resultado da comparação é bool
print(type("a" == "b"))       # 📄 comparando strings (False)
