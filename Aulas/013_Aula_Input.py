# 🧑‍💻 Captura dois números do usuário
valor_1 = input('Informe o primeiro número: ')
valor_2 = input('Informe o segundo número: ')

# 🔁 Converte os valores para inteiros
numero_1 = int(valor_1)
numero_2 = int(valor_2)

# ➕ Soma e exibe o resultado
print(f'Resultado da soma: {numero_1 + numero_2}')
