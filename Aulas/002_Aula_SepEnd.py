# 🔁 Quebras de linha:
# \r\n = CRLF (Windows) 💻
# \n   = LF (Linux/macOS) 🐧🍎

print(100, 200, 3000, sep='', end='>>')  # 🔧 Junta os números sem espaço e finaliza com >>
print(400, 500, sep=':', end='\n')       # 🔗 Separa com ':' e quebra a linha
print(600, 700, sep=':', end='\n')       # 🔗 Mesmo esquema acima
