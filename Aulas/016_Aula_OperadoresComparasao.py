# 🔍 Operadores de comparação (relacionais)
# Comparações básicas entre números e strings

igual_ou_maior = 3 >= 2  # ✅ maior ou igual
menor_que = 7 < 10       # 🔽 menor que
diferente_de = 'abc' != 'xyz'  # 🚫 diferente de
numero_igual = 5 == 5    # ⚖️ igual
string_igual = 'teste' == 'teste'  # ⚖️ igual

# Exibindo os resultados das comparações
print(igual_ou_maior)  # ✅
print(menor_que)       # 🔽
print(diferente_de)    # 🚫
print(numero_igual)    # ⚖️
print(string_igual)    # ⚖️

