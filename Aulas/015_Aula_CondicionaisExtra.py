# 🔍 Testando múltiplas condições
condicao_a = False
condicao_b = True
condicao_c = False
condicao_d = True

if condicao_a:
    print('Condição A foi ativada 🔴')
elif condicao_b:
    print('Condição B foi ativada 🟢')
elif condicao_c:
    print('Condição C foi ativada 🔵')
elif condicao_d:
    print('Condição D foi ativada 🟡')
else:
    print('Nenhuma condição foi atendida. ❌')

